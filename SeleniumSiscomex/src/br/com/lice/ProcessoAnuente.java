package br.com.lice;

public class ProcessoAnuente {
	
	String numProcessoAnuente;
	String siglaOrgaoAnuente;
	
	public ProcessoAnuente() {	
	}

	public String getNumProcessoAnuente() {
		return numProcessoAnuente;
	}

	public void setNumProcessoAnuente(String numProcessoAnuente) {
		this.numProcessoAnuente = numProcessoAnuente;
	}

	public String getSiglaOrgaoAnuente() {
		return siglaOrgaoAnuente;
	}

	public void setSiglaOrgaoAnuente(String siglaOrgaoAnuente) {
		this.siglaOrgaoAnuente = siglaOrgaoAnuente;
	}
	
	

}
